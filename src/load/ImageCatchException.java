public class ImageCatchException extends Exception {

    public ImageCatchException() {
        super();
    }

    public ImageCatchException(String message) {
        super(message);
    }

    public ImageCatchException(String message, Throwable cause) {
        super(message, cause);
    }

    public ImageCatchException(Throwable cause) {
        super(cause);
    }
}
